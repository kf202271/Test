package com_kk_2023_1;

public class Kk {
    private int age;
    private  String name;
    public Kk(){}

    public Kk(int age, String name) {
        this.age = age;
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
